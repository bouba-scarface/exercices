// # The sea with some whirlpools (30 by 9)
// And to spice things up, add an X at the positions 25:2 and 7:9 and a 0 at the positions 6:4 and 18:7
//
// ```
// ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
// ~~~~~~~~~~~~~~~~~~~~~~~~X~~~~~
// ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
// ~~~~~0~~~~~~~~~~~~~~~~~~~~~~~~
// ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
// ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
// ~~~~~~~~~~~~~~~~~0~~~~~~~~~~~~
// ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
// ~~~~~~X~~~~~~~~~~~~~~~~~~~~~~~
// ```

let ch = "";
const sea =[];
for(let i=0; i<9; i++){
  for(let j=0; j<30; j++){
    if( ((i === 1) && (j ===24)) || ( (i === 8) && (j === 6) ) ){
      ch +="X";
    }
    else if( ((i === 3) && (j === 5)) || ( (i === 6) && (j === 17) ) ){
      ch += "0";
    }
    else{
      ch +="~";
    }
  }
  sea.push(ch);
  ch="";
}
for(let i=0; i<9; i++){
  console.log(sea[i]);
}